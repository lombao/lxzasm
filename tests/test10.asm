# Amstrad Semanal 57

	ORG #A000
	LD HL,(POSIC)
	CALL #BB75
	LD HL,(NUMERO)
	SCF
	LD DE,10000
	INC HL
	LD A,47
DMIL: 	INC A
	SBC HL,DE
	JR NC,DMIL
	CALL PRINT
	LD DE,1000
MIL:	INC A
	SBC HL,DE
	JR NC,MIL
	CALL PRINT
	LD DE,100
CIEN:	INC A
	SBC HL,DE
	JR NC,CIEN
	CALL PRINT
	LD DE,10
DIEZ:	INC A
	SBC HL,DE
	JR NC,DIEZ
	CALL PRINT
	ADD A,L
	CALL PRINT
	RET
PRINT:  CALL #BB5A
	LD A,47
	JR NZ,PAS
	INC HL
PAS:	ADD HL,DE
	INC HL
	RET
POSIC:	DEFW #0505
NUMERO:	DEFW 9999
